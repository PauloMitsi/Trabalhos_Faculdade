public class Ex1f {
    public static void main(String arg[]) {
        int a = 0, b = 20;
        for (a = 0; a <= b; a++) {
            if (a < b) {
                System.out.println(a + " menor " + b);
            } else if (a > b) {
                System.out.println(a + " maior " + b);
            } else {
                System.out.println(a + " igual " + b);
            }
        }
    }
}
